package chapter1_intro.ujian;

public class Ujian1_3VolumeBalok {
    public static void main(String[] args) {
        int panjang = 5;
        int lebar = 6;
        int tinggi = 10;

        int VB =(panjang * lebar * tinggi);

        System.out.println("Volume Balok dengan Panjang "  +  panjang + " cm, lebar " + lebar  +  " cm dan Tinggi " +  tinggi + " adalah "  + VB + " cm3");
    }
}
